"""Generate the LaTeX fragments the report ``\input{...}``s.

We emit four files under ``docs/latex/figures/prediction/``:

  * ``results_macros.tex`` — ``\newcommand\ZuptTrainCoverage{...}`` etc.
    so the main body can reference the numbers and they stay in sync.
  * ``results_table_train.tex`` — tabular with headline metrics on train.
  * ``results_table_test.tex`` — same on the blind-test split.
  * ``results_per_experiment.tex`` — per-experiment breakdown.

The figures themselves are copied (or just referenced) from the
evaluation output directory so ``\includegraphics{prediction/...}``
works from ``main.tex``.
"""

from __future__ import annotations

import json
import shutil
from pathlib import Path

import pandas as pd


# ---------------------------------------------------------------------------
# Utility formatters
# ---------------------------------------------------------------------------

def _fmt_pct(x, places: int = 1) -> str:
    if x is None or pd.isna(x):
        return "--"
    return f"{100 * float(x):.{places}f}\\%"


def _fmt_num(x, places: int = 2) -> str:
    if x is None or pd.isna(x):
        return "--"
    return f"{float(x):.{places}f}"


def _escape_latex(s: str) -> str:
    return (s.replace("_", "\\_").replace("%", "\\%")
             .replace("&", "\\&").replace("#", "\\#"))


# ---------------------------------------------------------------------------
# Macro sheet
# ---------------------------------------------------------------------------

def _macro_name(base: str, split: str, algo: str, suffix: str) -> str:
    # LaTeX macros have to be purely alphabetic — normalise split/algo
    parts = [base.capitalize(),
             split.capitalize(),
             algo.replace("_", "").capitalize(),
             suffix.replace("_", "").capitalize()]
    return "\\" + "".join(parts)


_PCT_FIELDS = {
    "clean_coverage_90",
    "clean_frac_within_1_5m",
    "clean_frac_within_3m",
    "accepted_clean_coverage_90",
    "filter_accept_rate",
    "filter_accept_rate_clean",
    "filter_reject_rate_unclean",
}
_INT_FIELDS = {"n_total", "n_clean", "n_accepted_clean"}

_MACRO_FIELDS = [
    ("clean_coverage_90",             "CleanCov"),
    ("clean_median_ci",               "CleanCI"),
    ("clean_mean_ci",                 "CleanCIMean"),
    ("clean_median_abs_err",          "CleanMed"),
    ("clean_mae",                     "CleanMAE"),
    ("clean_rmse",                    "CleanRMSE"),
    ("clean_frac_within_1_5m",        "Within"),
    ("clean_frac_within_3m",          "WithinThree"),
    ("accepted_clean_coverage_90",    "AccCov"),
    ("accepted_clean_median_abs_err", "AccMed"),
    ("accepted_clean_median_ci",      "AccCI"),
    ("filter_accept_rate",            "AcceptAll"),
    ("filter_accept_rate_clean",      "AcceptClean"),
    ("filter_reject_rate_unclean",    "RejectUnclean"),
    ("n_clean",                       "NClean"),
    ("n_accepted_clean",              "NAcc"),
    ("n_total",                       "NTotal"),
]


def write_macros(
    per_split_metrics: dict[str, dict[str, dict]],
    out_path: Path,
) -> None:
    """``per_split_metrics[split][algo]`` = metrics dict (from MetricsBundle).

    Emits one ``\newcommand`` per field, named
    ``\<base><Split><Algo><Field>`` so LaTeX can say e.g.
    ``\ResultTrainZuptCleanCov`` or ``\ResultTestScurveCleanMAE``.
    """
    lines = ["% Auto-generated by src/prediction/evaluation/report.py\n"]
    for split, algos in per_split_metrics.items():
        for algo, m in algos.items():
            for key, short in _MACRO_FIELDS:
                val = m.get(key)
                if val is None or (isinstance(val, float) and val != val):
                    val_s = "--"
                elif key in _INT_FIELDS:
                    val_s = f"{int(val)}"
                elif key in _PCT_FIELDS:
                    val_s = _fmt_pct(val, places=1)
                else:
                    val_s = _fmt_num(val, places=2)
                name = _macro_name("Result", split, algo, short)
                lines.append(f"\\newcommand{{{name}}}{{{val_s}}}\n")
    out_path.write_text("".join(lines), encoding="utf-8")


# ---------------------------------------------------------------------------
# Headline metrics table
# ---------------------------------------------------------------------------

def write_metrics_table(
    algos_metrics: dict[str, dict],
    split: str,
    out_path: Path,
) -> None:
    """One compact tabular with headline numbers per algorithm."""
    rows = []
    for algo, m in algos_metrics.items():
        rows.append([
            algo.upper().replace("_", "-"),
            f"{int(m['n_clean'])}",
            f"{int(m['n_accepted_clean'])}",
            _fmt_pct(m["clean_coverage_90"]),
            _fmt_pct(m["accepted_clean_coverage_90"]),
            _fmt_num(m["clean_median_abs_err"]) + " m",
            _fmt_num(m["clean_median_ci"]) + " m",
            _fmt_pct(m["clean_frac_within_1_5m"]),
            _fmt_pct(m["filter_reject_rate_unclean"]),
        ])

    header = (
        "Algorithm & $n_\\mathrm{clean}$ & $n_\\mathrm{acc}$ & "
        "Cov (clean) & Cov (acc) & "
        "$\\tilde{|e|}$ & $\\tilde{w}$ & $\\le 1.5$ m & "
        "Rej. dirty"
    )

    out = [
        "% Auto-generated — headline metrics on the " + split + " split\n",
        "\\resizebox{\\textwidth}{!}{%\n",
        "\\begin{tabular}{lrrrrrrrr}\n",
        "\\toprule\n",
        header + " \\\\\n",
        "\\midrule\n",
    ]
    for r in rows:
        out.append(" & ".join(r) + " \\\\\n")
    out.append("\\bottomrule\n\\end{tabular}%\n}\n")
    out_path.write_text("".join(out), encoding="utf-8")


# ---------------------------------------------------------------------------
# Per-experiment table
# ---------------------------------------------------------------------------

def _short_exp_name(name: str) -> str:
    """Compact experiment label: experimenter-initial + building + phone-short."""
    # Format example: UriyaCohenEliya_beitYitzchakiRaanana_GooglePixel10_15-04-2026_exp6
    parts = name.split("_")
    if len(parts) < 3:
        return name[:24]
    experimenter = parts[0]
    # initials: UriyaCohenEliya → UCE, eyalyakir → EY, RoyTurgeman → RT
    letters = [c for c in experimenter if c.isupper()]
    initials = "".join(letters) if letters else experimenter[:2].upper()
    building = parts[1]
    phone = parts[2]
    # compact phone tag
    phone_map = {
        "GooglePixel10": "Pix10",
        "SamsungSM-S911B": "S23",
        "SamsungSM-A235F": "A23",
        "Xiaomi22101320I": "Xmi",
        "SamsungGalaxyZFlip6": "ZF6",
        "Pixel10": "Pix10",
    }
    phone_tag = phone_map.get(phone, phone[:6])
    # strip common suffixes
    if building.startswith("beit"):
        building = building[4:]
    if building.endswith("Raanana"):
        building = building[:-7]
    if building.startswith("BarIlan"):
        building = building.replace("Herzelia", "")
    building = building[:14]
    return f"{initials}/{building}/{phone_tag}"


def write_per_experiment_table(
    dfs_by_algo: dict[str, pd.DataFrame],
    split: str,
    out_path: Path,
) -> None:
    """One tabular with per-experiment MAE / coverage for each algorithm
    on the given split.

    Only experiments with at least one clean segment are included, and
    we restrict to rows whose ``experiment_type`` matches ``split``.
    The result is wrapped in ``\\resizebox{\\textwidth}{!}{...}`` so it
    always fits inside the page margins regardless of algorithm count.
    """
    from .metrics import per_experiment_metrics

    tables = {algo: per_experiment_metrics(
                df[df["experiment_type"] == split])
              for algo, df in dfs_by_algo.items()}
    exps = sorted(set().union(*[set(t["exp_name"]) for t in tables.values()]))

    algos = list(dfs_by_algo.keys())
    col_spec = "l" + "rrr" * len(algos)

    header_cols = ["\\textbf{Experiment}"]
    for a in algos:
        header_cols += [f"{a.upper()} MAE", f"{a.upper()} Cov.", f"$\\tilde{{w}}$"]

    out = [
        f"% Auto-generated per-experiment table ({split})\n",
        "\\resizebox{\\textwidth}{!}{%\n",
        f"\\begin{{tabular}}{{{col_spec}}}\n\\toprule\n",
        " & ".join(header_cols) + " \\\\\n\\midrule\n",
    ]
    for exp in exps:
        label = _short_exp_name(exp)
        row = [f"\\texttt{{{_escape_latex(label)}}}"]
        for algo in algos:
            tab = tables[algo]
            row_df = tab[tab["exp_name"] == exp]
            if row_df.empty:
                row += ["--", "--", "--"]
            else:
                r = row_df.iloc[0]
                row += [
                    _fmt_num(r["mae"]) + " m",
                    _fmt_pct(r["coverage_90"]),
                    _fmt_num(r["median_ci"]) + " m",
                ]
        out.append(" & ".join(row) + " \\\\\n")
    out.append("\\bottomrule\n\\end{tabular}%\n}\n")
    out_path.write_text("".join(out), encoding="utf-8")


# ---------------------------------------------------------------------------
# Figure staging
# ---------------------------------------------------------------------------

def stage_figures(
    source_dirs: dict[str, Path],
    dest_dir: Path,
    prefix: str = "",
) -> list[Path]:
    """Copy every ``fig_*.png`` from each source dir into ``dest_dir``
    with a ``<prefix><source_key>_<name>`` filename. Returns the list
    of destination paths.
    """
    dest_dir.mkdir(parents=True, exist_ok=True)
    copied = []
    for key, src in source_dirs.items():
        if not Path(src).exists():
            continue
        for png in Path(src).glob("fig_*.png"):
            out = dest_dir / f"{prefix}{key}_{png.stem.removeprefix('fig_')}.png"
            shutil.copy2(png, out)
            copied.append(out)
    return copied


# ---------------------------------------------------------------------------
# Top-level
# ---------------------------------------------------------------------------

def build_report_assets(
    train_root: Path,
    test_root: Path | None,
    latex_assets_dir: Path,
) -> None:
    """Gather everything the report needs into one folder ready for
    ``\includegraphics``.
    """
    latex_assets_dir.mkdir(parents=True, exist_ok=True)

    # --- load metrics
    per_split: dict[str, dict[str, dict]] = {}
    with open(train_root / "metrics_train.json", "r") as f:
        train_json = json.load(f)
    per_split["train"] = {a: v["metrics"] for a, v in train_json.items()}

    if test_root is not None and (test_root / "metrics_test.json").exists():
        with open(test_root / "metrics_test.json", "r") as f:
            per_split["test"] = json.load(f)

    # --- macros
    write_macros(per_split, latex_assets_dir / "results_macros.tex")

    # --- tables
    write_metrics_table(
        per_split["train"], "train",
        latex_assets_dir / "results_table_train.tex",
    )
    if "test" in per_split:
        write_metrics_table(
            per_split["test"], "test",
            latex_assets_dir / "results_table_test.tex",
        )

    # --- per-experiment table (combines train & test)
    dfs: dict[str, pd.DataFrame] = {}
    for algo in ("zupt", "trapezoid"):
        frames = []
        pt = train_root / f"predictions_{algo}_train.csv"
        if pt.exists(): frames.append(pd.read_csv(pt))
        if test_root is not None:
            pe = test_root / f"predictions_{algo}_test.csv"
            if pe.exists(): frames.append(pd.read_csv(pe))
        if frames:
            dfs[algo] = pd.concat(frames, ignore_index=True)

    if dfs:
        write_per_experiment_table(
            dfs, "train", latex_assets_dir / "results_per_experiment_train.tex",
        )
        if "test" in per_split:
            write_per_experiment_table(
                dfs, "test", latex_assets_dir / "results_per_experiment_test.tex",
            )

    # --- figures: copy flat
    src: dict[str, Path] = {}
    for algo in ("zupt", "trapezoid"):
        src[f"train_{algo}"] = train_root / f"figures_{algo}"
        if test_root is not None:
            src[f"test_{algo}"] = test_root / f"figures_{algo}"
    stage_figures(src, latex_assets_dir, prefix="")

    # --- cross-algorithm comparison plot(s)
    for png in train_root.glob("fig_compare*.png"):
        shutil.copy2(png, latex_assets_dir / f"train_{png.name}")
    if test_root is not None:
        for png in test_root.glob("fig_compare*.png"):
            shutil.copy2(png, latex_assets_dir / f"test_{png.name}")
